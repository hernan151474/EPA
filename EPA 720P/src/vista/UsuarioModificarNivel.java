    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        usuario = ctrlUsuario.leerUsuario(txtUsuario.getText());
        if(usuario!=null)
        {
            nombreUsuario = txtUsuario.getText();
            nivelUsuario = usuario.getNivel();
            idUsuario = usuario.getIdUsuario();
            contraseñaUsuario = usuario.getContraseña();
            int confirmar,respuesta;
            confirmar = JOptionPane.YES_NO_OPTION;
            respuesta = JOptionPane.showConfirmDialog(null,"¿Desea modificar el nivel?","Modificar",confirmar);
            if (respuesta == 0 )
            {
                modificarNivel();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Este Usuario no existe");
            txtUsuario.setBorder(new LineBorder(Color.RED, 2));
            txtUsuario.requestFocus();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


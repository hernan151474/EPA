    public Boolean validar()
    {
        txtUsuario.setBorder(new JTextField().getBorder());
        txtContraseña.setBorder(new JTextField().getBorder());
        Boolean validar = false;
        Boolean mostrarMsj = false;
        if(txtUsuario.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Debe ingresar Usuario");
            txtUsuario.setBorder(new LineBorder(Color.RED, 2));
            txtUsuario.requestFocus();
            mostrarMsj = true;
        }else
            if(cbxNivel.getSelectedIndex()!= nivelUsuario && mostrarMsj == false)
            {
                JOptionPane.showMessageDialog(null, "Seleccione correctamente el nivel actual");
                cbxNivel.requestFocus();
                mostrarMsj = true;
            }else
               if(cbxNivel.getSelectedIndex() == cbxNuevoNivel.getSelectedIndex() && mostrarMsj == false)
                {
                    JOptionPane.showMessageDialog(null, "Nuevo nivel es igual a nivel actual");
                    cbxNuevoNivel.requestFocus();
                    mostrarMsj = true;
                }else
                    if(txtContraseña.getPassword().length == 0 && mostrarMsj == false)
                    {
                        JOptionPane.showMessageDialog(null, "Debe ingresar Contraseña");
                        txtContraseña.setBorder(new LineBorder(Color.RED, 2));
                        txtContraseña.requestFocus();
                        mostrarMsj = true;
                    }else{
                        validar = true;
                    }    
        return validar;
    }
    
    public void modificarNivel()
    {
        if(validar() == true)
        {
            usuario = new Usuario();
            usuario.setIdUsuario(idUsuario);
            usuario.setUsuario(nombreUsuario);
            if(String.valueOf(txtContraseña.getPassword()).equals(contraseñaUsuario))
            {
                usuario.setContraseña(contraseñaUsuario);
                usuario.setNivel(cbxNuevoNivel.getSelectedIndex());
                ctrlUsuario.modificarUsuario(usuario);
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Contraseña incorrecta");
                txtContraseña.requestFocus();
            }
        }
    }
    
    public void limpiar()
    {
        txtUsuario.setText("");
        txtContraseña.setText("");
        cbxNuevoNivel.setSelectedIndex(0);
        cbxNivel.setSelectedIndex(0);
        txtUsuario.requestFocus();
        nivelUsuario = 0;
        idUsuario = 0;
        contraseñaUsuario = "";
        nombreUsuario = "";
    }


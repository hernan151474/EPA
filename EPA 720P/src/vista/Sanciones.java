    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        sancion = new Sancion();
        sancion.setIdSancion(sancione.get(punto).getIdSancion());

        int confirmar = JOptionPane.YES_NO_OPTION;
        int respuesta = JOptionPane.showConfirmDialog(null, "<html>¿Está seguro que quiere eliminar esta Sanción:<br><b> "
                + sancione.get(punto).getSancion() + "</b></br>?</html>", "Advertencia", confirmar, JOptionPane.WARNING_MESSAGE);
        if (respuesta == 0) {
            ctrlSancion.eliminar(sancion);
            JOptionPane.showMessageDialog(null, "ELIMINADO CON EXITO");
            limpiar();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblSancionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSancionMouseClicked
        punto = tblSancion.rowAtPoint(evt.getPoint());
        txtSancion.setText(sancione.get(punto).getSancion());
        modif = true;
        btnEliminar.setEnabled(true);
    }//GEN-LAST:event_tblSancionMouseClicked
    private void cargarTableSancion() {
        model = (DefaultTableModel) tblSancion.getModel();
        model.setRowCount(0);
        Object[] fila = new Object[1];
        sancione = ctrlSancion.leerTodos();

        for (int i = 0; i < sancione.size(); i++) {
            fila[0] = sancione.get(i).getSancion();
            model.addRow(fila);
        }
        tblSancion.setModel(model);
    }

    private void limpiar() {
        txtSancion.setText(null);
        sancione.clear();
        cargarTableSancion();
        modif = false;
        txtSancion.requestFocus();
        btnEliminar.setEnabled(false);
    }



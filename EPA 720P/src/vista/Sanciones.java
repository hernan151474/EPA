    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtSancion.getText().isEmpty()) {
            sancion = new Sancion();
            sancion.setSancion(txtSancion.getText());
            if (modif == true) {
                sancion.setIdSancion(sancione.get(punto).getIdSancion());
                ctrlSancion.modificar(sancion);
            } else {
                ctrlSancion.crear(sancion);
            }
            JOptionPane.showMessageDialog(this, "Guardado");
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "Complete el cuadro de Texto Sanción");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


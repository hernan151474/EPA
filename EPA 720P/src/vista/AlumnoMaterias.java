    private void cargarTabla() {
        listAula = ctrlAula.leer();
        model = (DefaultTableModel) tblAula.getModel();
        model.setRowCount(0);
        Object[] fila = new Object[3];
        for (int i = 0; i < listAula.size(); i++) {
            fila[0] = listAula.get(i).getCurso().getCurso();
            fila[1] = listAula.get(i).getDivision().getDivision();
            fila[2] = listAula.get(i).getTurno().getTurno();
            model.addRow(fila);
        }
        tblAula.setModel(model);
    }

    private void cargarComboPlanEst() {
        listPlanEst = ctrlPlanEstudio.leer();
        for (int i = 0; i < listPlanEst.size(); i++) {
            cbxPlanEst.addItem(listPlanEst.get(i).getTitulo());
        }
    }

    private void cargarCampos() {

        alumno = ctrlAlumno.leer(txtCuil.getText());
        if (alumno != null) {
            btnGuardar.setEnabled(true);
            txtApeNom.setText(alumno.getPersonal().getApellido() + " " + alumno.getPersonal().getNombre());
            txtDomicilio.setText("En proceso");
        } else {
            JOptionPane.showMessageDialog(this, "No se econtró Alumno", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void guardar() {
        materia = new Materia();
        materia.setPlanEstudio(listPlanEst.get(cbxPlanEst.getSelectedIndex()));
        alumnoMateria = new AlumnoMateria();
        alumnoMateria.setAlumno(alumno);
        alumnoMateria.setMateria(materia);
        alumnoMateria.setAula(listAula.get(punto));
        alumnoMateria.setCicloLectivo(Integer.parseInt(txtCiclo.getText()));
        ctrlAlumnoMateria.crear(alumnoMateria);//Solo toma de un aula
        //REALIZAR CODIGO QUE ACTUALICE EL PROMEDIO
        limpiar();
    }

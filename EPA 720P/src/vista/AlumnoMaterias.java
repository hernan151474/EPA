    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (alumno == null) {
            JOptionPane.showMessageDialog(this, "Primero Cargue un Alumno", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (txtCiclo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete el campo Ciclo Lectivo", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (seleccion == false) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Aula", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            guardar();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed


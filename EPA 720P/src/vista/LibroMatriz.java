    public static void cargarTabla() {
        listAlumnoMateria = ctrlAlumnoMateria.leerAlumnoLibro(alumno.getIdAlumno());
        model = (DefaultTableModel) tblEspacioCurricular.getModel();
        model.setRowCount(0);
        Object[] fila = new Object[6];
        for (int i = 0; i < listAlumnoMateria.size(); i++) {
            notas = ctrlNotas.leerAlumno(listAlumnoMateria.get(i).getIdAlumnoMateria());
            fila[0] = listAlumnoMateria.get(i).getMateria().getAño();
            fila[1] = listAlumnoMateria.get(i).getMateria().getMateria();
            //fila[1] = listAlumnoMateria.get(i).getPromedio();
            fila[2] = notas.getNota();
            if (notas.getNota() >= 6) {
                fila[3] = "REGULAR";
            } else {
                fila[3] = "DESAPROBADO";
            }

            fila[4] = meses[notas.getFecha().getMonth()].toUpperCase();
            fila[5] = notas.getFecha().getYear() + 1900;
            model.addRow(fila);
        }
        btnImprimir.setEnabled(true);
        tblEspacioCurricular.setModel(model);
    }


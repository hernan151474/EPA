    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed

        List<ListaLibroMatriz> lista = new ArrayList();

        DefaultTableModel modelJasper = (DefaultTableModel) tblEspacioCurricular.getModel();
        JRTableModelDataSource dataSource = new JRTableModelDataSource(modelJasper);
        String reportSource = "src/reportes/libroMatriz.jrxml";

        for (int i = 0; i < tblEspacioCurricular.getRowCount(); i++) {
            System.out.println(tblEspacioCurricular.getValueAt(i, 0).toString());
            ListaLibroMatriz libro = new ListaLibroMatriz(Integer.parseInt(tblEspacioCurricular.getValueAt(i, 0).toString()),
                    tblEspacioCurricular.getValueAt(i, 1).toString(),
                    Float.parseFloat(tblEspacioCurricular.getValueAt(i, 2).toString()),
                    tblEspacioCurricular.getValueAt(i, 3).toString(),
                    tblEspacioCurricular.getValueAt(i, 4).toString(),
                    Integer.parseInt(tblEspacioCurricular.getValueAt(i, 5).toString()));
            lista.add(libro);
        }

        Object obj = "libroMatriz.jadper";
        try {
            JasperReport jr = JasperCompileManager.compileReport(reportSource);
            Map<String, Object> parametros = new HashMap<>();
            parametros.put("legajo", txtLegajo.getText());
            parametros.put("cuil", txtCuil.getText());
            parametros.put("apeNom", txtApeNom.getText());
            parametros.put("fecha", txtFechaIngreso.getText());

            parametros.put("Columna_anioCurso", modelJasper.getColumnName(0));
            parametros.put("Columna_EspCurr", modelJasper.getColumnName(1));
            parametros.put("Columna_Calif", modelJasper.getColumnName(2));
            parametros.put("Columna_Cond", modelJasper.getColumnName(3));
            parametros.put("Columna_Mes", modelJasper.getColumnName(4));
            parametros.put("Columna_Anio", modelJasper.getColumnName(5));
            JasperPrint jp = JasperFillManager.fillReport(jr, parametros, new JRBeanCollectionDataSource(lista));
            JasperViewer.viewReport(jp, false);

        } catch (JRException ex) {
            Logger.getLogger(LibroMatriz.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnImprimirActionPerformed
